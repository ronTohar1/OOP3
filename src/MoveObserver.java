public class MoveObserver {

    IMover mover;
    public void MoveUp(Unit unit){
        mover.MoveUp(unit);
    }
    public void MoveDown(Unit unit){
        mover.MoveUp(unit);
    }
    public void MoveLeft(Unit unit){
        mover.MoveUp(unit);
    }
    public void MoveRight(Unit unit){
        mover.MoveUp(unit);
    }


}
